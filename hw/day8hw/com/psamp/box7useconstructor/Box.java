package com.psamp.box7useconstructor;

public class Box {
	
	double height;
	double width;
	double depth;
	
	Box(double w, double h, double d) {
		System.out.println("Constructing box...");
		width = w;
		height = h;
		depth = d;
	}
	
	double volume(String boxName) {
		 double vol;
		 vol = width * height * depth;
		 System.out.println("The volume of " + boxName + " is " + vol + ".");
		 return 0;
	 }
}
